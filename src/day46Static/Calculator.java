package day46Static;

public class Calculator {

	/*
	 * Calculator:
	static methods:
		plus
		minus
		multiply

    all of them:
    	return type: double
    	params: 2 double numbers
	 * 
	 */
	
	public static double plus (double num1, double num2) {
		return num1 + num2;
	}
	
	public static double minus (double num1, double num2) {
		return num1 - num2;
	}
	
	
	public static double multiply (double num1, double num2) {
		return num1 * num2;
	}
	
}
